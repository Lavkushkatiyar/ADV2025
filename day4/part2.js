export const Directions = {
  N: (x, y, arr) => {
    if (x === 0) return false;
    return arr[x - 1][y] === "@";
  },

  S: (x, y, arr) => {
    if (x === arr.length - 1) return false;
    return arr[x + 1][y] === "@";
  },

  W: (x, y, arr) => {
    if (y === 0) return false;
    return arr[x][y - 1] === "@";
  },

  E: (x, y, arr) => {
    if (y === arr[x].length - 1) return false;
    return arr[x][y + 1] === "@";
  },

  NE: (x, y, arr) => {
    if (x === 0 || y === arr[x].length - 1) return false;
    return arr[x - 1][y + 1] === "@";
  },

  NW: (x, y, arr) => {
    if (x === 0 || y === 0) return false;
    return arr[x - 1][y - 1] === "@";
  },

  SE: (x, y, arr) => {
    if (x === arr.length - 1 || y === arr[x].length - 1) return false;
    return arr[x + 1][y + 1] === "@";
  },

  SW: (x, y, arr) => {
    if (x === arr.length - 1 || y === 0) return false;
    return arr[x + 1][y - 1] === "@";
  },
};

const removeRolls = (ogArr, fillArr) => {
  for (const a of fillArr) {
    const [i, j] = a;
    ogArr[i][j] = "X";
  }
};

export const countAdjecent = (x, y, arr) => {
  let count = 0;
  for (const dir in Directions) {
    if (Directions[dir](x, y, arr)) count++;
  }
  return count;
};

export const findAdjecents = (arr, fillArr) => {
  let result = 0;

  for (let i = 0; i < arr.length; i++) {
    for (let j = 0; j < arr[i].length; j++) {
      if (arr[i][j] !== "@") continue;
      if (countAdjecent(i, j, arr) < 4) {
        fillArr.push([i, j]);
        result++;
      }
    }
  }
  return result;
};

export const findRollPaper = (inputs) => {
  let sum = 0;
  const arr = [];
  let fillArr = [];
  const input = inputs.split("\n");

  for (const line of input) {
    arr.push([...line]);
  }
  let x = findAdjecents(arr, fillArr);
  while (x !== 0) {
    sum += x;
    removeRolls(arr, fillArr);
    fillArr = [];
    x = findAdjecents(arr, fillArr);
  }
  return sum;
};
